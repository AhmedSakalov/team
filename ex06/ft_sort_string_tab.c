void	ft_sort_string_tab(char **tab)
{
	int i;
	char *x;

	i = 0;
	while (tab[i] != '\0')
	{
		if (tab[i] < tab[i + 1])
			i++;
		else
		{
			x = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = x;
			i--;
		}
	}
}
